    private void Cadastra() {
        String cpf, nome, senha, email, endereco, data, telefone;
        int conta, agencia;
        try {
            DataRegistra();
        } catch (ParseException ex) {
            Logger.getLogger(InternalCadastro.class.getName())
                    .log(Level.SEVERE, null, ex);
        }

        if (maioridade == true) {
            String itemSelecionado = (String) txtEstado.getSelectedItem();
            cpf = txtCpf.getText().toUpperCase();
            nome = txtNome.getText().toUpperCase();
            email = txtEmail.getText().toUpperCase();
            senha = txtSenha.getText();
            endereco = txtRua.getText().toUpperCase() + ", " + 
                    txtNum.getText().toUpperCase() + " - " + itemSelecionado +
                    ", " + txtCEP.getText().toUpperCase();
            telefone = txtTelefone.getText();
            
            
            UsuarioDTO objUsuarioDTO = new UsuarioDTO();
            objUsuarioDTO.setId_CadastCPF(cpf);
            objUsuarioDTO.setId_CadastNome(nome);
            objUsuarioDTO.setId_CadastEmail(email);
            objUsuarioDTO.setId_CadastSenha(senha);
            objUsuarioDTO.setId_CadastEndereco(endereco);
            objUsuarioDTO.setId_Telefone(telefone);

            UsuarioDAO objUsuarioDAO = new UsuarioDAO();
            objUsuarioDAO.CadastrarUsuario(objUsuarioDTO);
            
            
            // Verificar idade do cliente
            
            TelaLogin tl = new TelaLogin();
            tl.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Você não tem idade para"
                    + " criar uma conta no banco Rubik");
        }
        LimparCampo();
    }
    
