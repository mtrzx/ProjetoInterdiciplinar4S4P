    public void DataRegistra() throws ParseException {
        UsuarioDTO usuarioDTO = new UsuarioDTO();
        UsuarioDAO objUsuarioDAO = new UsuarioDAO();
        String dataEU = nascimentoInput.getText(); 

        // Define o formato da data europeia
        SimpleDateFormat formatoEuropeu = new SimpleDateFormat("dd/MM/yyyy");
        LocalDate dataAtual = LocalDate.now();

        // Converte a data europeia em um objeto Date
        Date data = formatoEuropeu.parse(dataEU);

        // Define o formato da data ISO
        SimpleDateFormat formatoISO = new SimpleDateFormat("yyyy-MM-dd");

        // Converte a data Date para o formato ISO
        String dataISO = formatoISO.format(data);
        Date dataFormatada = formatoISO.parse(dataISO);
        
        usuarioDTO.setId_CadastDate(dataISO);
        
        // Calcula a diferença de idade
        Period diferenca = Period.between(
                dataFormatada.toInstant().atZone
        (ZoneId.systemDefault()).toLocalDate(), dataAtual);

        if (diferenca.getYears() >= 18) {
            maioridade = true; 
        } else {
            maioridade = false;
        }
           
    }
    
    private void verificaSenha (){
        if (txtSenha.getText().length() <6) {
            JOptionPane.showMessageDialog(null, "Senha muito curta, sua senha deve conter ao menos 6 digitos");
        }else if(txtSenha.getText().length()>15){
            JOptionPane.showMessageDialog(null, "Senha muito longa, sua senha deve conter até 15 digitos");
        }else {
            senhaLength = true;
        }
        
        
    }
    
